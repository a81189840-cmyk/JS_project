document.getElementById("generateBill").addEventListener("click", function () {

    const customerName =
        document.getElementById("customerName").value.trim();

    const mobile =
        document.getElementById("mobileNumber").value.trim();

    const day =
        document.getElementById("day").value;

    const coupon =
        document.getElementById("coupon").value.trim().toUpperCase();

    const payment =
        document.getElementById("payment").value;

    if (customerName === "") {

        alert("Enter Customer Name");
        return;

    }

    if (!/^[0-9]{10}$/.test(mobile)) {

        alert("Enter Valid Mobile Number");
        return;

    }

    if (day === "") {

        alert("Please Select Day");
        return;

    }

    const qtyInputs =
        document.querySelectorAll(".qty-input");

    let orderedItems = [];

    let subtotal = 0;

    qtyInputs.forEach(input => {

        const qty =
            parseInt(input.value);

        if (qty > 0) {

            const price =
                Number(input.dataset.price);

            const amount =
                qty * price;

            subtotal += amount;

            orderedItems.push({

                name: input.dataset.name,

                qty: qty,

                price: price,

                amount: amount

            });

        }

    });

    if (orderedItems.length === 0) {

        alert("Please Select Food Items");

        return;

    }

    // Coupon

    let discount = 0;

    if (coupon === "SAVE10") {

        discount = subtotal * 0.10;

    }

    else if (coupon === "SAVE20") {

        discount = subtotal * 0.20;

    }

    // GST

    const gst =
        (subtotal - discount) * 0.05;

    const finalTotal =
        subtotal - discount + gst;

    // Order Number

    const orderNo =
        Math.floor(Math.random() * 900000) + 100000;

    // Date & Time

    const now = new Date();

    const date =
        now.toLocaleDateString();

    const time =
        now.toLocaleTimeString();

    // Invoice

    let bill = `

    <div class="invoice">

        <h1>🍽 INDIAN FOOD HOUSE</h1>

        <p>
        <b>Order No :</b> ${orderNo}
        </p>

        <p>
        <b>Date :</b> ${date}
        </p>

        <p>
        <b>Time :</b> ${time}
        </p>

        <hr>

        <p>
        <b>Customer :</b> ${customerName}
        </p>

        <p>
        <b>Mobile :</b> ${mobile}
        </p>

        <p>
        <b>Day :</b> ${day}
        </p>

        <p>
        <b>Payment :</b> ${payment}
        </p>

        <table>

        <tr>

        <th>Item</th>

        <th>Qty</th>

        <th>Price</th>

        <th>Total</th>

        </tr>

    `;

    orderedItems.forEach(item => {

        bill += `

        <tr>

        <td>${item.name}</td>

        <td>${item.qty}</td>

        <td>₹${item.price}</td>

        <td>₹${item.amount}</td>

        </tr>

        `;

    });

    bill += `

        </table>

        <br>

        <h3>
        Sub Total :
        ₹${subtotal.toFixed(2)}
        </h3>

        <h3>
        Discount :
        ₹${discount.toFixed(2)}
        </h3>

        <h3>
        GST (5%) :
        ₹${gst.toFixed(2)}
        </h3>

        <h2>
        Grand Total :
        ₹${finalTotal.toFixed(2)}
        </h2>

        <hr>

        <h3>
        😊 Thank You For Visiting 😊
        </h3>

        <p>

        "Serving Happiness, One Plate at a Time."

        </p>

        <p>

        We Hope To See You Again!

        </p>

        <p>

        📞 +91-9876543210

        </p>

        <p>

        📧 indianfoodhouse@gmail.com

        </p>

        <button onclick="window.print()">

        🖨 Print Bill

        </button>

    </div>

    `;

    document.getElementById("bill").innerHTML = bill;

});